            <p>Hsab Sabon est une application de gestion financiere collaborative destinee aux colocataires. Le service
                a pour objet de faciliter le suivi, le calcul, et la transparence des depenses partagees entre personnes
                vivant en communaute, principalement au Maroc.</p>
            <p>Le service propose notamment les fonctionnalites suivantes :</p>
            <ul>
                <li>Creation de comptes utilisateurs personnels avec authentification securisee</li>
                <li>Creation et gestion de groupes de colocation (colocs)</li>
                <li>Invitation de membres par e-mail dans une colocation existante</li>
                <li>Enregistrement detaille des depenses avec categorisation (loyer, eau, electricite, internet,
                    courses, menage, divertissement, etc.)</li>
                <li>Attribution des depenses a un ou plusieurs payeurs</li>
                <li>Definition de parts personnalisees pour chaque depense (repartition egale, par pourcentage, ou par
                    montant fixe)</li>
                <li>Calcul automatique des equilibres et des dettes entre colocataires</li>
                <li>Visualisation des bilans sous forme de tableaux et graphiques</li>
                <li>Suivi des remboursements et des paiements effectues entre membres</li>
                <li>Historique complet des transactions par colocation</li>
                <li>Export des donnees en format CSV ou PDF</li>
                <li>Notifications par e-mail des activites importantes (nouvelle depense, solde a payer, etc.)</li>
                <li>Application web responsive accessible sur ordinateur, tablette et smartphone</li>
